package io.shiftleft.codepropertygraph.generated.nodes

import io.shiftleft.codepropertygraph.generated.Language.*
import scala.collection.immutable.{IndexedSeq, ArraySeq}

trait CommentEMT extends AnyRef with AstNodeEMT with HasFilenameEMT

trait CommentBase extends AbstractNode with AstNodeBase with StaticType[CommentEMT] {

  override def propertiesMap: java.util.Map[String, Any] = {
    import io.shiftleft.codepropertygraph.generated.accessors.Lang.*
    val res = new java.util.HashMap[String, Any]()
    res.put("CODE", this.code)
    this.columnNumber.foreach { p => res.put("COLUMN_NUMBER", p) }
    res.put("FILENAME", this.filename)
    this.lineNumber.foreach { p => res.put("LINE_NUMBER", p) }
    res.put("ORDER", this.order)
    res
  }
}

object Comment {
  val Label = "COMMENT"
  object PropertyNames {
    val Code         = io.shiftleft.codepropertygraph.generated.PropertyNames.CODE
    val ColumnNumber = io.shiftleft.codepropertygraph.generated.PropertyNames.COLUMN_NUMBER
    val Filename     = io.shiftleft.codepropertygraph.generated.PropertyNames.FILENAME
    val LineNumber   = io.shiftleft.codepropertygraph.generated.PropertyNames.LINE_NUMBER
    val Order        = io.shiftleft.codepropertygraph.generated.PropertyNames.ORDER
  }
  object PropertyDefaults {
    val Code     = "<empty>"
    val Filename = "<empty>"
    val Order    = -1: Int
  }
}

class Comment(graph_4762: flatgraph.Graph, seq_4762: Int)
    extends StoredNode(graph_4762, 9.toShort, seq_4762)
    with CommentBase
    with AstNode
    with StaticType[CommentEMT] {

  override def productElementName(n: Int): String =
    n match {
      case 0 => "code"
      case 1 => "columnNumber"
      case 2 => "filename"
      case 3 => "lineNumber"
      case 4 => "order"
      case _ => ""
    }

  override def productElement(n: Int): Any =
    n match {
      case 0 => this.code
      case 1 => this.columnNumber
      case 2 => this.filename
      case 3 => this.lineNumber
      case 4 => this.order
      case _ => null
    }

  override def productPrefix = "Comment"
  override def productArity  = 5

  override def canEqual(that: Any): Boolean = that != null && that.isInstanceOf[Comment]
}

